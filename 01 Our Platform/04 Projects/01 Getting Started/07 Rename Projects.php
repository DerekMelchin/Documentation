<p>Follow these steps to rename projects:</p>
<ol>
    <li><a href="/docs/v2/our-platform/projects/project-management#02-View-All-Projects">Open the project</a> you want to rename.</li>
    <li>In the Project panel, hover over the project name and then click the <span class='icon-name'>pencil</span> icon that appears.</li>
    <img class='docs-image' style='max-height: 120px' src="https://cdn.quantconnect.com/i/tu/rename-project-1.png">
    <li>In the <span class='field-name'>Name</span> field, enter the new project name and then click <span class='button-name'>Save Changes</span>.</li>
</ol>
