<p>Follow these steps to set your preferred organization:</p>

<ol>
    <li>Log in to the Algorithm Lab.</li>
    <li>In the top navigation bar, click <span class='button-name'>Connected as: <span class='placeholder-text'>organizationName</span></span>.</li>
    <li>In the Switch Organization panel, select the radio button under the <span class='column-name'>Preferred</span> column that corresponds to the organization that you want to set as the preferred organization.</li>
    <p>"Preferred organization selected" displays. Refresh the page to connect as your preferred organization.</p>
</ol>
