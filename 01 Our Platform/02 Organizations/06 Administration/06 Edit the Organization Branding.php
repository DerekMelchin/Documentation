<p>You can edit your organization's image and name.</p>

<h4>Image</h4>
<p>Follow these steps to change your organization image:</p>
<ol>
    <li>Open the <a href='/docs/v2/our-platform/organizations/administration#05-View-the-Organization-Homepage'>organization homepage</a>.</li>
    <li>Click the organization image.</li>
    <li>Click <span class='button-name'>Choose file</span>, select a file from your device, and then click <span class='button-name'>Open</span>.</li>
        <p>Your organization image must be in <span class='public-file-name'>gif</span>, <span class='public-file-name'>jpg</span>, or <span class='public-file-name'>png</span> format and less than 1MB in size.</p>
    <li>Click <span class='button-name'>Save</span>.</li>
    <p>"Photo Uploaded" displays.</p>
</ol>

