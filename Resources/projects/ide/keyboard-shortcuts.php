<p>Keyboard shortcuts are combinations of keys that you can issue to manipulate the IDE. They can speed up your workflow because they remove the need for you to reach for your mouse.</p>

<p>Follow these steps to view the keyboard shortcuts of your account:</p>

<ol>
    <li><a href='<?=$projectLink?>'>Open a project</a>.</li>
    <li>Press <span class='key-combinations'>F1</span>.</li>
    <li>Enter \"Preferences: Open Keyboard Shortcuts\".</li>
    <li>Click <span class='button-name'>Preferences: Open Keyboard Shortcuts</span>.</li>
</ol>

<p>To set a key binding for a command, click the <span class='icon-name'>pencil</span> icon in the left column of the keyboard shortcuts table, enter the key combination, and then press <span class='key-combinations'>Enter</span>.  

